-- select 5 oldest users
select * from 
    (select * from users order by created_at)
where rownum <= 5;

-- what day of the week do most people register
select to_char(created_at, 'DAY') as Day, count(*) as Nr_Created from users
group by to_char(created_at, 'DAY')
order by Nr_Created desc;

-- find users who have never posted a photo
select users.user_name from users
left join photos
    on users.id = photos.user_id
where photos.id is null
order by users.id asc;

-- find maximum liked photo and its creator and like count
select
    users.id,
    user_name,
    photos.id as photo_id,
    image_url,
    count(*)
from photos
inner join likes
    on photos.id = likes.photo_id
inner join users
    on photos.user_id = users.id
group by users.id, user_name, photos.id, image_url
order by count(*) desc
fetch next 1 row only;

-- how many times does the average user post
select
(select count(*) from photos) / (select count(*) from users) as AVG_POST_PER_PERSON from DUAL;

-- top 5 most commonly used hashtags
select
    tags.id,
    tags.tag_name,
    count(*) as NR_OF_TIMES_USED
from tags
inner join photo_tags
    on tags.id = photo_tags.tag_id
group by tags.id, tags.tag_name
order by count(*) desc
fetch next 5 rows only;

-- find users who have liked every single photo on the site
select 
    users.id,
    user_name,
    count(*)
from likes
inner join users
    on likes.user_id = users.id
group by users.id, user_name
having count(*) = (select count(*) from photos);

-- find the location that has been visited the most
select location, count(*) from location_indexes
group by location
having count(*) = 
(
    select max(count(*)) from location_indexes
    group by location
);

-- find who searched the most popular search index
select user_id, users.user_name from search_indexes
inner join users
    on users.id = search_indexes.user_id
where text in 
(
    select text from search_indexes
    group by text
    having count(*) = 
    (
        select max(count(*)) from search_indexes
        group by text
    )
);

-- find the photo which has the most comments, show comments photo and user who commented
select photo_id, comment_text, user_id from comments
where photo_id in 
(
    select photo_id from comments
    group by photo_id
    having count(*) = 
    (
       select max(count(*)) from comments
       group by photo_id
    )
);
 
