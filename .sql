-- list how many followers does each user have
select users.user_name, count(*) from follows
inner join users
    on users.id = follows.followee_id
group by followee_id, users.user_name;

-- select users who are inactive(no photos, no likes given)
select users.user_name from users
left join photos
    on users.id = photos.user_id
left join likes
    on users.id = likes.user_id
where photos.id is null and likes.user_id is null
group by users.user_name;

-- select most active user(s) (most photos, most likes)
select id, user_name from
(
    select users.id, users.user_name from users
    inner join photos
        on users.id = photos.user_id
    group by users.user_name, users.id
    having count(*) = 
    (
        select max(count(*)) from photos
        group by user_id
    )
)
inner join likes
    on id = likes.user_id
group by id, user_name
having count(*) = 
(
    select max(count(*)) from likes
    group by user_id
);

